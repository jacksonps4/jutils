package com.minorityhobbies.util;

class SmtpResetException extends Exception {
	private static final long serialVersionUID = 8696008572213455117L;

	public SmtpResetException() {
		super();
	}

	public SmtpResetException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SmtpResetException(String arg0) {
		super(arg0);
	}

	public SmtpResetException(Throwable arg0) {
		super(arg0);
	}
}
